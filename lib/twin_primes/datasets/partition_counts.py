"""
partition_counts — partition {1..N} into W, A, B, X for BCM analysis.

  W  A002822 twin prime witnesses (ground truth)
  A  BCM-discovered witnesses (accumulator; A ⊆ W)
  B  boundary set: candidates generated by the enqueue rule that failed
     the primality check (false witnesses that fell out of A)
  X  exception set: integers swept past without ever being generated
     as a candidate — the true escapes

W, B, X partition {1..N} exactly. A ⊆ W.

Usage (as module):
    from twin_primes.datasets.partition_counts import generate
    data = generate(limit=400_000)

Usage (as script):
    python -m twin_primes.datasets.partition_counts [LIMIT] [--output PATH]
    python -m twin_primes.datasets.partition_counts 400000 --output data/partition-counts.json
"""

import heapq

from ..sieve import prime_sieve


def generate(*, limit=400_000):
    """
    Run BCM to `limit` and count the W/A/B/X partition of {1..limit}.

    Parameters
    ----------
    limit : int
        Upper bound (inclusive) of the range to partition.

    Returns
    -------
    dict with keys:
        limit   int
        W       {"count": int, "fraction": float}   A002822 witnesses
        A       {"count": int, "fraction": float}   BCM-discovered
        missed  {"count": int, "fraction": float}   |W| - |A|
        B       {"count": int, "fraction": float}   boundary: enqueue candidates that failed
        X       {"count": int, "fraction": float}   exception: swept without being generated
    """
    N = limit
    sieve_arr = prime_sieve(6 * N + 1)

    def is_witness(w):
        return bool(sieve_arr[6*w-1]) and bool(sieve_arr[6*w+1])

    # --- BCM run ---
    # Track A (witnesses) and B (enqueue candidates that failed primality check)
    A = [1]
    A_set = {1}
    B_set = set()
    q = [1]

    while q:
        v = heapq.heappop(q)
        for u in A:
            s = u + v
            if s > N:
                break
            if s in A_set or s in B_set:
                continue
            if is_witness(s):
                A.append(s)
                A_set.add(s)
                heapq.heappush(q, s)
            else:
                B_set.add(s)

    # --- Full witness set W ---
    W_set = {m for m in range(1, N + 1) if is_witness(m)}

    # --- X: everything not in W or B (swept past, never generated as candidate) ---
    X_set = set(range(1, N + 1)) - W_set - B_set

    missed = W_set - A_set

    def _entry(s):
        return {"count": len(s), "fraction": round(len(s) / N, 8)}

    return {
        "limit":  N,
        "W":      _entry(W_set),
        "A":      _entry(A_set),
        "missed": _entry(missed),
        "B":      _entry(B_set),
        "X":      _entry(X_set),
    }


if __name__ == "__main__":
    import argparse
    import json
    import sys

    p = argparse.ArgumentParser(
        description="Partition {1..N} into W/A/B/X for BCM analysis"
    )
    p.add_argument("limit", type=int, nargs="?", default=400_000,
                   help="Upper bound N (default: 400000)")
    p.add_argument("--output", default="-",
                   help="Output file path (default: stdout)")
    args = p.parse_args()

    data = generate(limit=args.limit)
    out = json.dumps(data, indent=2)

    if args.output == "-":
        print(out)
    else:
        with open(args.output, "w") as f:
            f.write(out)
        print(f"Wrote {args.output}", file=sys.stderr)
